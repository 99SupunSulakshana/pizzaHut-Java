    public void sales() throws SQLException
    {
        int lastid = 0;
  
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/pizza","root","");
          
            String total = txttotal.getText();
            String balance = txtbal.getText();
            String pay = payment.getText();
            
            String query = "INSERT INTO sales(total, pay, balance) VALUES (?,?,?)";
            pst = (PreparedStatement) con.prepareStatement(query,Statement.RETURN_GENERATED_KEYS);
            pst.setString(1, total);
            pst.setString(2, pay);
            pst.setString(3, balance);
            pst.executeUpdate();
            ResultSet rs = pst.getGeneratedKeys();
            
            if(rs.next())
            {
                lastid = rs.getInt(1);
            }
            
            int row = jtable.getRowCount();
            String query1 = "INSERT INTO sales_products(sales_id, pname, price, qty, total) VALUES (?,?,?,?,?)";
            pst1 = (PreparedStatement) con.prepareStatement(query1);
            
            String pname = "";
            int price;
            int qty;
            int tot = 0;
            
            for(int i=0; i<jtable.getRowCount(); i++)
            {
                pname = (String)jtable.getValueAt(i, 0);
                price = (int)jtable.getValueAt(i, 1);
                qty = (int)jtable.getValueAt(i, 2);
                tot = (int)jtable.getValueAt(i, 3);
                
                pst1.setInt(1, lastid);
                pst1.setString(2, pname);
                pst1.setInt(3, price);
                pst1.setInt(4, qty);
                pst1.setInt(5, tot);
                pst1.executeUpdate();
                
            }
            JOptionPane.showMessageDialog(this, "Sales Details Added Completelly!!!!!!!!!!!!!!!!!");
            
            HashMap a = new HashMap();
            a.put("invo",lastid);
            
            JasperDesign jdesign = JRXmlLoader.load("C:\\Users\\supun\\OneDrive\\Documents\\NetBeansProjects\\PizzaProject\\src\\META-INF\\persistence.xml");
            JasperReport jreport = JasperCompileManager.compileReport(jdesign);
            
            JasperPrint jprint = JasperFillManager.fillReport(jreport,a , con);
            
            JasperViewer.viewReport(jprint);
            
            
        } catch (ClassNotFoundException ex) {
            java.util.logging.Logger.getLogger(Main.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        } catch (JRException ex) {
            java.util.logging.Logger.getLogger(Main.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
 
    }
