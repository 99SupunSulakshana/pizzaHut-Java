    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
       
        if(small.isSelected() == true)
        {
            item = "Small";
            price = 200;  
        }
        else if(medium.isSelected() == true)
        {
            item = "Medium";
            price = 500; 
        }
        else if(large.isSelected() == true)
        {
            item = "Large";
            price = 1000; 
        }
        else if(extra.isSelected() == true)
        {
            item = "Extra Large";
            price = 2000; 
        }
        
        int qty = Integer.parseInt(txtqty.getValue().toString());
        int tot = qty * price;
        
        model = (DefaultTableModel)jtable.getModel();
        model.addRow(new Object[]
        {
            item,
            price,
            qty,
            tot
        });
        
        int sum = 0;
        
        for(int a=0; a<jtable.getRowCount(); a++)
        {
            sum = sum + Integer.parseInt(jtable.getValueAt(a,3).toString());
        }
        
        txttotal.setText(Integer.toString(sum));
    }//GEN-LAST:event_addbtnActionPerformed

